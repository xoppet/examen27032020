public class EjercicioJunit {

	 public int a, b;
	 
	 public void setA(int a){
	     this.a = a;    
	 }
	 
	 public void setB(int b){
	     this.b = b;
	 }

	 public int sumar(){
	     return a+b;
	 }
	 
	 public int restar(){
	     return a-b;
	 }

	 public int dividir(){
	     return a/b;
	 }

	 public int multiplicar(){
	     return a*b;
	 }
}


